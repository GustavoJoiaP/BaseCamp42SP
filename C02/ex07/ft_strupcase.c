char	*ft_strupcase(char *str)
{
	int		i;
	char	flag;

	i = 0;
	while (str[i] != '\0')
	{
		flag = str[i];
		if (str[i] >= 97 && str[i] <= 122)
		{
			str[i] = flag - 32;
		}
		i++;
	}
	return (str);
}
